    public void HienThi(){
       
        DefaultTableModel model =  (DefaultTableModel)dgvMobile.getModel();
        model.setRowCount(0);
        for (Singer m : list.getList()) {
            
             model.addRow(new Object[]{m.getSingerName(),m.getMember(),m.getCountry(),m.getGender(),m.getCompany()});
        }
       
    }
    
    
