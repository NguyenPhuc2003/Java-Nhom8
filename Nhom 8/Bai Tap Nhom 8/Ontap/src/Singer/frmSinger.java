    public Singer khoitao()
    {
            Singer singer = new Singer();
            singer.setSingerName(txtName.getText());
            singer.setMember(Integer.parseInt(txtMember.getText()));
             singer.setGender (txtGender.getText());
            singer.setCountry(txtCountry.getText());
            singer.setCompany(txtCompany.getText());
            return singer;
    }
